numbers = [1, 2, 3, 4, 5]

numbers.delete_at(1) # delete_at(index) deletes the element at the specified index and returns that element. In this case the element at index 1 (2) is deleted and returned.
numbers.delete(1) # delete(obj) deletes all items from the numbers array that equal 1 and returns the last deleted item. In this case the element 1 is deleted and returned.
